namespace AngleSharp.DOM.Css
{
    /// <summary>
    /// A list of possible CSS value list separators.
    /// </summary>
    enum CssValueListSeparator
    {
        /// <summary>
        /// A space separating two entities.
        /// </summary>
        Space,
        /// <summary>
        /// A slash separating two entitites.
        /// </summary>
        Slash,
        /// <summary>
        /// A comma separating two entitites.
        /// </summary>
        Comma,
    }
}
